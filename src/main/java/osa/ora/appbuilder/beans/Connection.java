package osa.ora.appbuilder.beans;

/**
 *
 * @author Osama Redhat
 */
public class Connection {
    private String id;
    private String from;
    private String to;
    private String text;
    private String fromX;
    private String toX;
    private String fromY;
    private String toY;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the from
     */
    public String getFrom() {
        return from;
    }

    /**
     * @param from the from to set
     */
    public void setFrom(String from) {
        this.from = from;
    }

    /**
     * @return the to
     */
    public String getTo() {
        return to;
    }

    /**
     * @param to the to to set
     */
    public void setTo(String to) {
        this.to = to;
    }

    /**
     * @return the text
     */
    public String getText() {
        return text;
    }

    /**
     * @param text the text to set
     */
    public void setText(String text) {
        this.text = text;
    }

    /**
     * @return the fromX
     */
    public String getFromX() {
        return fromX;
    }

    /**
     * @param fromX the fromX to set
     */
    public void setFromX(String fromX) {
        this.fromX = fromX;
    }

    /**
     * @return the toX
     */
    public String getToX() {
        return toX;
    }

    /**
     * @param toX the toX to set
     */
    public void setToX(String toX) {
        this.toX = toX;
    }

    /**
     * @return the fromY
     */
    public String getFromY() {
        return fromY;
    }

    /**
     * @param fromY the fromY to set
     */
    public void setFromY(String fromY) {
        this.fromY = fromY;
    }

    /**
     * @return the toY
     */
    public String getToY() {
        return toY;
    }

    /**
     * @param toY the toY to set
     */
    public void setToY(String toY) {
        this.toY = toY;
    }
    
}
